(
    id            int(20) NOT NULL COMMENT 'Product id',
    category_id   int(20)      DEFAULT NULL COMMENT 'category id',
    item_type     varchar(100) DEFAULT NULL COMMENT 'Product series',
    title         varchar(100) DEFAULT NULL COMMENT 'Product title',
    sell_point    varchar(300) DEFAULT NULL COMMENT 'Commodity selling point',
    price         bigint(20)   DEFAULT NULL COMMENT 'Commodity unit price',
    num           int(10)      DEFAULT NULL COMMENT 'stock quantity',
    image         varchar(500) DEFAULT NULL COMMENT 'image path',
    status        int(1)       DEFAULT '1' COMMENT 'Product status 1: on the shelf 2: off the shelf 3: delete',
    priority      int(10)      DEFAULT NULL COMMENT 'Display priority',
    created_time  datetime     DEFAULT NULL COMMENT 'created time',
    modified_time datetime     DEFAULT NULL COMMENT 'last modification time',
    created_user  varchar(50)  DEFAULT NULL COMMENT 'Created by',
    modified_user varchar(50)  DEFAULT NULL COMMENT 'last modified person',
